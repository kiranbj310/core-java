class Furniture{

	static String furnitureType;
	static String furnitureBrand;
	static double furnitureCost;


	static void setFurnitureType(String ft){
		furnitureType=ft;
	}
	static String getFurnitureType(){
		return furnitureType;
	}
	
	static void setFurnitureBrand(String fb){
		furnitureBrand=fb;
	}
	static String getFurnitureBrand(){
		return furnitureBrand;	
	}
	static void setFurnitureCost(double fc){
		furnitureCost=fc;
	}
	static double getFurnitureCost(){
		return furnitureCost;
	}	

}