class Galaxy{

	static String name;
	static String consistOf;
	static String planet;
			 
			 
	static void setName(String nm){
	    name = nm;
	}		 
    static String getName(){
		return name;
	}
    static void setConsistOf(String co){
	    consistOf = co;
	}
    static String getConsistOf(){
	    return consistOf;
	}
    static void setPlanet(String pl){
		planet = pl;
	}
    static String getPlanet(){
		return planet;
	}
}