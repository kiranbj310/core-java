class  Paint {

    static int paintId ;
	static String color ;
	static String brandName ;


    static void setPaintId(int id){
		paintId = id ;	
	}
	static int getPaintId(){
		return paintId ;
	}
	static void setColor(String cl){
		color = cl ; 
	} 	
	static String getColor(){
		return color ;
	}	
	static void setBrandName(String name){
		brandName = name ; 
	} 	
	static String getBrandName(){
		return brandName ;
	}
	}