class Vessel{

	static String material;
	static int price;
	static String color;
			
			
        static void setMaterial(String ml){
			material = ml;
		}
        static String getMaterial(){
		    return material;
		}
        static void setPrice(int pr){
			price = pr;
		}
		static int getPrice(){
			return price;
		}
        static void setColor(String cl){
	        color = cl;
		}
        static String getColor(){
		    return color;
		}
  
}